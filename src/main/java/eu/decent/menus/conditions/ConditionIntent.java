/**
 * This enum represents all the different intentions, that actions and conditions can be set on.
 */
public enum ConditionIntent {
    DISPLAY,
    CLICK,
    LEFT_CLICK,
    RIGHT_CLICK,
    SHIFT_LEFT_CLICK,
    SHIFT_RIGHT_CLICK,
    MIDDLE_CLICK,
    DROP
}